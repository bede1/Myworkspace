package d;

public class ForFunction2 {
	public static void main(String[] args) {
		int [] numbers = {20,30,40,50,60};
		for(int x : numbers){
		System.out.print(x);
		System.out.print(",");
		//System.out.print("\n");
		String [] names = {"james","lorry","sherma","lacy"};
		for(String name : names) {
			System.out.print(name);
			System.out.print(",");
		
		}
		System.out.print("\n");
		}
		}
	}


